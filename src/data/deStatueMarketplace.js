function slugify(text) {
  return String(text || "")
    .normalize("NFKD")
    .replace(/[^̀-ͯ]/g, (m) => m)
    .replace(/[^\p{L}\p{N}]+/gu, "_")
    .replace(/^_+|_+$/g, "")
    .replace(/_+/g, "_")
    .toLowerCase();
}

export function getAllGermanStatueSets() {
  return DE_STATUE_MARKETPLACE.categories.flatMap((category) =>
    category.sets.map((set) => ({
      ...set,
      categoryKey: category.key,
      categoryTitle: category.title,
      categoryIcon: category.icon,
      displayTitle: `${category.title} · ${set.title}`,
      lessonType: "bonus_statue",
      lessonTypeLabel: "Statue Bonus",
      lessonTypeIcon: "🗿",
    }))
  );
}

export function getGermanStatueCategories() {
  return DE_STATUE_MARKETPLACE.categories;
}

export function getGermanStatueSet(setKey) {
  for (const category of DE_STATUE_MARKETPLACE.categories) {
    const found = category.sets.find((s) => s.key === setKey);
    if (found) {
      return {
        ...found,
        categoryKey: category.key,
        categoryTitle: category.title,
        categoryIcon: category.icon,
      };
    }
  }
  return null;
}

export function buildGermanStatueLesson(setKey) {
  const set = getGermanStatueSet(setKey);
  if (!set) return null;

  const vocab = set.items.map((item) => ({
    de: item.phrase,
    en: item.meaning,
    example: item.example,
  }));

  const dialogue = [];
  set.items.forEach((item) => {
    dialogue.push({ speaker: "Guide", text: item.example });
    dialogue.push({ speaker: "Guide", text: item.explain });
  });

  return {
    id: `de-statue-market-${set.key}`,
    title: `${set.categoryTitle} — ${set.title}`,
    section: "Statue Bonus",
    unit: "Cultural Bonus",
    lesson: "Bonus",
    lesson_focus: `${set.categoryTitle} · ${set.title}`,
    icon: "🗿",
    xp: 60,
    grammar: `Cultural bonus lesson: ${set.categoryTitle}. Learn natural German that textbooks usually skip.`,
    vocab,
    dialogue,
    lessonType: "bonus_statue",
    lessonTypeLabel: "Statue Bonus",
    lessonTypeIcon: "🗿",
    audioFolder: "statues",
    recommendedExercises: 10,
    statueSetKey: set.key,
    statueMeta: {
      categoryTitle: set.categoryTitle,
      title: set.title,
      items: set.items.map((item) => ({
        ...item,
        audio: {
          phrase: `/audio/de/statues/${slugify(item.phrase)}.mp3`,
          sentence: `/audio/de/statues/${slugify(item.phrase)}_sentence.mp3`,
          explanation: `/audio/de/statues/${slugify(item.phrase)}_explained.mp3`,
        },
      })),
    },
  };
}
